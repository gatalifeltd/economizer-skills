#!/usr/bin/env python3
"""Aggregate the heavy multi-turn benchmark (runs/heavy/manifest.jsonl).

Per-turn token accounting differs by provider:
  - codex : JSONL; turn.completed.usage.input_tokens already includes resent
            history (grows per turn). total = input + output + reasoning.
  - cursor: single JSON; fresh inputTokens + cacheReadTokens (resent history)
            + outputTokens. The compounding shows up in cacheReadTokens.
We sum the per-turn totals into a session total, then compare WITH vs WITHOUT
per agent. The headline is the within-agent reduction on cumulative tokens.
"""
import json, os, statistics
from collections import defaultdict

HERE = os.path.dirname(os.path.abspath(__file__))
MANIFEST = os.path.join(HERE, "runs", "heavy", "manifest.jsonl")


def turn_total(agent, path):
    """Return tokens processed on one turn for the given provider raw file."""
    if not os.path.exists(path):
        return 0
    text = open(path, encoding="utf-8", errors="replace").read()
    if agent == "codex":
        best = 0
        for line in text.splitlines():
            line = line.strip()
            if '"usage"' not in line:
                continue
            try:
                o = json.loads(line)
            except Exception:
                continue
            u = o.get("usage") or (o.get("msg", {}) or {}).get("usage")
            if isinstance(u, dict) and "input_tokens" in u:
                t = u.get("input_tokens", 0) + u.get("output_tokens", 0) + u.get("reasoning_output_tokens", 0)
                best = max(best, t)
        return best
    else:  # cursor-family (cursor/opus/sonnet)
        try:
            o = json.loads(text, strict=False)
        except Exception:
            return 0
        u = o.get("usage", {}) or {}
        return u.get("inputTokens", 0) + u.get("cacheReadTokens", 0) + u.get("outputTokens", 0)


def main():
    if not os.path.exists(MANIFEST):
        raise SystemExit("no runs/heavy/manifest.jsonl — run run_heavy.sh first")
    rows = [json.loads(l) for l in open(MANIFEST) if l.strip()]

    # per (agent,cond): list of session totals; and per-turn cumulative curves
    sess = defaultdict(list)
    curves = defaultdict(list)   # (agent,cond) -> list of cumulative-curve lists
    models = {}
    for r in rows:
        a, c = r["agent"], r["condition"]
        models[a] = r.get("model", "?")
        per_turn = [turn_total(a, os.path.join(HERE, p)) for p in r["raws"]]
        cum, run = [], 0
        for t in per_turn:
            run += t; cum.append(run)
        if cum and cum[-1] > 0:
            sess[(a, c)].append(cum[-1])
            curves[(a, c)].append(cum)

    agents = sorted({a for a, _ in sess})

    def mean(xs): return statistics.mean(xs) if xs else 0

    lines = ["# Heavy Multi-Turn Benchmark Results", "",
             "_Cumulative tokens over a persistent multi-turn session on a ~120k-token "
             "codebase. Generated by `analyze_heavy.py`. See `README.md` for method/limits._", ""]
    nturns = rows[0]["turns"] if rows else 0
    lines += [f"- Agents / models: " + ", ".join(f"**{a}** (`{models[a]}`)" for a in agents),
              f"- Turns per session: {nturns}",
              f"- Reps per cell: {max((len(sess[(a,'with')]) for a in agents), default=0)}", ""]

    lines += ["## Cumulative session tokens — WITH vs WITHOUT policy", "",
              "| Agent | Without | With | Reduction |",
              "|-------|--------:|-----:|----------:|"]
    chart = []
    for a in agents:
        wo, wi = mean(sess.get((a, "without"), [])), mean(sess.get((a, "with"), []))
        red = round((wo - wi) / wo * 100, 1) if wo else None
        lines.append(f"| {a} | {wo:,.0f} | {wi:,.0f} | {'—' if red is None else str(red)+'%'} |")
        chart.append((a, wo, wi))

    lines += ["", "![cumulative growth](chart_heavy.svg)", "",
              "_Lines: cumulative tokens vs turn. Solid = without policy, dashed = with. "
              "A flatter line = less transcript bloat resent each turn._", ""]
    open(os.path.join(HERE, "RESULTS_heavy.md"), "w").write("\n".join(lines) + "\n")
    write_chart(agents, curves)
    print("wrote RESULTS_heavy.md and chart_heavy.svg")
    for a in agents:
        print(f"  {a}: without={mean(sess.get((a,'without'),[])):,.0f}  with={mean(sess.get((a,'with'),[])):,.0f}")


def write_chart(agents, curves):
    W, H, padl, padb, padt = 720, 420, 70, 40, 50
    colors = ["#3b82f6", "#16a34a", "#db2777", "#f59e0b"]
    # average curves per (agent,cond)
    def avg_curve(cs):
        if not cs: return []
        n = min(len(c) for c in cs)
        return [statistics.mean(c[i] for c in cs) for i in range(n)]
    series = []
    maxv = maxt = 1
    for i, a in enumerate(agents):
        for cond, dash in (("without", ""), ("with", "6,5")):
            cur = avg_curve(curves.get((a, cond), []))
            if cur:
                series.append((a, cond, dash, colors[i % len(colors)], cur))
                maxv = max(maxv, max(cur)); maxt = max(maxt, len(cur))
    svg = [f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 {W} {H}" font-family="sans-serif">']
    svg.append(f'<text x="{W/2}" y="26" text-anchor="middle" font-size="15" font-weight="700">Cumulative tokens over a multi-turn session</text>')
    x0, y0, x1, y1 = padl, padt, W - 20, H - padb
    svg.append(f'<line x1="{x0}" y1="{y1}" x2="{x1}" y2="{y1}" stroke="#999"/>')
    svg.append(f'<line x1="{x0}" y1="{y0}" x2="{x0}" y2="{y1}" stroke="#999"/>')
    def px(t): return x0 + (t / max(1, maxt - 1)) * (x1 - x0)
    def py(v): return y1 - (v / maxv) * (y1 - y0)
    for v in (0, maxv/2, maxv):
        svg.append(f'<text x="{x0-6}" y="{py(v)+3}" text-anchor="end" font-size="9" fill="#666">{v/1000:.0f}k</text>')
    for t in range(maxt):
        svg.append(f'<text x="{px(t)}" y="{y1+14}" text-anchor="middle" font-size="9" fill="#666">{t+1}</text>')
    legy = y0
    for a, cond, dash, color, cur in series:
        pts = " ".join(f"{px(i):.1f},{py(v):.1f}" for i, v in enumerate(cur))
        da = f' stroke-dasharray="{dash}"' if dash else ""
        svg.append(f'<polyline fill="none" stroke="{color}" stroke-width="2"{da} points="{pts}"/>')
        svg.append(f'<text x="{x1-150}" y="{legy}" font-size="10" fill="{color}">{a} {cond}</text>')
        legy += 14
    svg.append(f'<text x="{(x0+x1)/2}" y="{H-6}" text-anchor="middle" font-size="10" fill="#666">turn</text>')
    open(os.path.join(HERE, "chart_heavy.svg"), "w").write("\n".join(svg) + "\n</svg>\n")


if __name__ == "__main__":
    main()
