#!/usr/bin/env python3
"""Aggregate the 3-arm output-heavy benchmark (runs/min/manifest.jsonl).

Reports input / output / total tokens per arm (without / full / min) per agent,
and the total-token reduction of full and min vs without. The story: 'min' keeps
the output-shrinking benefit with almost none of the file-weight cost.
"""
import json, os, statistics
from collections import defaultdict

HERE = os.path.dirname(os.path.abspath(__file__))
MANIFEST = os.path.join(HERE, "runs", "min", "manifest.jsonl")


def parse(agent, path):
    if not os.path.exists(path):
        return None
    text = open(path, encoding="utf-8", errors="replace").read()
    if agent == "codex":
        inp = out = 0
        for line in text.splitlines():
            if '"usage"' not in line:
                continue
            try:
                u = json.loads(line).get("usage", {})
            except Exception:
                continue
            if "input_tokens" in u:
                inp = max(inp, u.get("input_tokens", 0))
                out = max(out, u.get("output_tokens", 0) + u.get("reasoning_output_tokens", 0))
        return (inp, out)
    else:
        try:
            u = json.loads(text, strict=False).get("usage", {})
        except Exception:
            return None
        return (u.get("inputTokens", 0) + u.get("cacheReadTokens", 0), u.get("outputTokens", 0))


def mean(xs): return statistics.mean(xs) if xs else 0


def main():
    if not os.path.exists(MANIFEST):
        raise SystemExit("no runs/min/manifest.jsonl — run run_min.sh first")
    rows = [json.loads(l) for l in open(MANIFEST) if l.strip()]
    inp = defaultdict(list); out = defaultdict(list); tot = defaultdict(list)
    models = {}
    for r in rows:
        if r.get("exit_code", 1) != 0:
            continue
        m = parse(r["agent"], os.path.join(HERE, r["raw"]))
        if not m or m[1] == 0:
            continue
        models[r["agent"]] = r.get("model", "?")
        k = (r["agent"], r["condition"])
        inp[k].append(m[0]); out[k].append(m[1]); tot[k].append(m[0] + m[1])

    agents = sorted({a for a, _ in tot})
    conds = ["without", "full", "min"]
    lines = ["# Minimal-Policy Benchmark (output-heavy tasks)", "",
             "_3 arms: no policy / full 12-rule policy / 3-rule minimal policy. "
             "Generated by `analyze_min.py`._", "",
             f"- Agents / models: " + ", ".join(f"**{a}** (`{models.get(a,'?')}`)" for a in agents),
             f"- Tasks: " + ", ".join(sorted({r['task'] for r in rows})),
             f"- Reps per cell: up to {max((r['rep'] for r in rows), default=0)}", "",
             "## Mean tokens per arm", "",
             "| Agent | Arm | Input | Output | Total | Δ total vs without |",
             "|-------|-----|------:|-------:|------:|-------------------:|"]
    for a in agents:
        base = mean(tot.get((a, "without"), []))
        for c in conds:
            if (a, c) not in tot:
                continue
            tt = mean(tot[(a, c)])
            d = "—" if c == "without" or not base else f"{(base-tt)/base*100:+.1f}%"
            lines.append(f"| {a} | {c} | {mean(inp[(a,c)]):,.0f} | {mean(out[(a,c)]):,.0f} | {tt:,.0f} | {d} |")

    lines += ["", "## Output tokens only (the channel the model controls)", "",
              "| Agent | without | full | min | full Δout | min Δout |",
              "|-------|--------:|-----:|----:|----------:|---------:|"]
    chart = []
    for a in agents:
        wo = mean(out.get((a, "without"), [])); fu = mean(out.get((a, "full"), [])); mi = mean(out.get((a, "min"), []))
        fd = f"{(wo-fu)/wo*100:+.1f}%" if wo else "—"
        md = f"{(wo-mi)/wo*100:+.1f}%" if wo else "—"
        lines.append(f"| {a} | {wo:,.0f} | {fu:,.0f} | {mi:,.0f} | {fd} | {md} |")
        chart.append((a, mean(tot.get((a,"without"),[])), mean(tot.get((a,"full"),[])), mean(tot.get((a,"min"),[]))))

    out_chart = [(a, mean(out.get((a, "without"), [])), mean(out.get((a, "full"), [])),
                  mean(out.get((a, "min"), []))) for a in agents]

    lines += ["", "### Output tokens by arm — the economy (lower = better)", "",
              "![output by arm](chart_min_output.svg)", "",
              "### Total tokens by arm (dominated by noisy, cached input)", "",
              "![total by arm](chart_min.svg)", ""]
    open(os.path.join(HERE, "RESULTS_min.md"), "w").write("\n".join(lines) + "\n")
    write_chart(chart, "chart_min.svg", "Total tokens by policy arm (input-dominated)")
    write_chart(out_chart, "chart_min_output.svg",
                "Output tokens by policy arm — lower is cheaper (output ≈ 5× input)",
                highlight="min")
    print("wrote RESULTS_min.md, chart_min.svg, chart_min_output.svg")
    for a, w, f, m in chart:
        print(f"  {a}: total without={w:,.0f} full={f:,.0f} min={m:,.0f}")


def write_chart(data, fname, title, highlight=None):
    W, H, pad = 680, 370, 56
    maxv = max([max(w, f, m) for _, w, f, m in data] + [1])
    bw, gap = 24, 150
    cols = {"without": "#bbb", "full": "#f59e0b", "min": "#16a34a"}
    svg = [f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 {W} {H}" font-family="sans-serif">']
    svg.append(f'<text x="{W/2}" y="24" text-anchor="middle" font-size="14" font-weight="700">{title}</text>')
    base = H - pad
    svg.append(f'<line x1="{pad}" y1="{base}" x2="{W-pad}" y2="{base}" stroke="#999"/>')
    for i, (a, w, f, m) in enumerate(data):
        x = pad + 30 + i * gap
        for j, (lab, v) in enumerate([("without", w), ("full", f), ("min", m)]):
            h = (v / maxv) * (H - 2 * pad) if maxv else 0
            bx = x + j * (bw + 3)
            svg.append(f'<rect x="{bx}" y="{base-h}" width="{bw}" height="{h}" fill="{cols[lab]}"/>')
            svg.append(f'<text x="{bx+bw/2}" y="{base-h-4}" text-anchor="middle" font-size="8">{v:,.0f}</text>')
            # savings % for the highlighted arm vs without
            if highlight and lab == highlight and w:
                svg.append(f'<text x="{bx+bw/2}" y="{base-h-15}" text-anchor="middle" font-size="9" font-weight="700" fill="#16a34a">{(w-v)/w*100:+.0f}%</text>')
        svg.append(f'<text x="{x+bw}" y="{base+16}" text-anchor="middle" font-size="11" font-weight="600">{a}</text>')
    lx = W - pad - 90
    for k, (lab, col) in enumerate(cols.items()):
        svg.append(f'<rect x="{lx}" y="{38+k*14}" width="10" height="10" fill="{col}"/>')
        svg.append(f'<text x="{lx+14}" y="{47+k*14}" font-size="10">{lab}</text>')
    if highlight:
        svg.append(f'<text x="{W/2}" y="{H-8}" text-anchor="middle" font-size="9" fill="#16a34a">green % = output saved by the 3-rule "min" set vs no policy</text>')
    open(os.path.join(HERE, fname), "w").write("\n".join(svg) + "\n</svg>\n")


if __name__ == "__main__":
    main()
