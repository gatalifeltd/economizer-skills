#!/usr/bin/env python3
"""Aggregate runs/manifest.jsonl into RESULTS.md + chart.svg.

Token/cost parsing lives here (not in run.sh) so it is easy to fix per agent
after inspecting a real raw run. Each agent emits a different shape:
  - claude : single JSON object  (.usage, .total_cost_usd)
  - codex  : JSONL event stream  (usage accumulates across lines)
  - cursor : JSON result         (usage fields, schema discovered at runtime)
We use agent-specific fast paths plus a generic recursive fallback, so missing
fields degrade to None rather than crashing.
"""
import json, os, re, statistics
from collections import defaultdict

HERE = os.path.dirname(os.path.abspath(__file__))
MANIFEST = os.path.join(HERE, "runs", "manifest.jsonl")

IN_KEYS    = {"input_tokens", "prompt_tokens", "inputtokens", "input"}
OUT_KEYS   = {"output_tokens", "completion_tokens", "outputtokens", "output"}
REASON_KEYS = {"reasoning_output_tokens", "reasoning_tokens"}
TOT_KEYS   = {"total_tokens", "total_token_usage", "totaltokens", "tokens_used"}
COST_KEYS  = {"total_cost_usd", "cost_usd", "cost", "total_cost"}


def _walk(obj, acc):
    """Collect best numeric candidates for in/out/total/cost from arbitrary JSON."""
    if isinstance(obj, dict):
        for k, v in obj.items():
            lk = k.lower()
            if isinstance(v, (int, float)):
                if lk in IN_KEYS:     acc["input"]  = max(acc.get("input", 0), v)
                if lk in OUT_KEYS:    acc["output"] = max(acc.get("output", 0), v)
                if lk in REASON_KEYS: acc["reason"] = max(acc.get("reason", 0), v)
                if lk in TOT_KEYS:    acc["total"]  = max(acc.get("total", 0), v)
                if lk in COST_KEYS:   acc["cost"]   = max(acc.get("cost", 0.0), float(v))
            _walk(v, acc)
    elif isinstance(obj, list):
        for v in obj:
            _walk(v, acc)


def parse_metrics(agent, text):
    acc = {}
    # Try as one JSON object first (claude, cursor), else line-by-line (codex).
    parsed_any = False
    try:
        _walk(json.loads(text), acc)
        parsed_any = True
    except Exception:
        for line in text.splitlines():
            line = line.strip()
            if not line:
                continue
            try:
                _walk(json.loads(line), acc)
                parsed_any = True
            except Exception:
                continue
    inp = acc.get("input")
    out = acc.get("output")
    rea = acc.get("reason")
    tot = acc.get("total")
    if tot is None and (inp is not None or out is not None):
        tot = (inp or 0) + (out or 0) + (rea or 0)
    return {"input": inp, "output": out, "reason": rea, "total": tot,
            "cost": acc.get("cost"), "parsed": parsed_any}


def load():
    rows = []
    if not os.path.exists(MANIFEST):
        raise SystemExit("no runs/manifest.jsonl — run run.sh first")
    for line in open(MANIFEST):
        line = line.strip()
        if not line:
            continue
        r = json.loads(line)
        raw_path = os.path.join(HERE, r["raw"])
        text = open(raw_path, encoding="utf-8", errors="replace").read() if os.path.exists(raw_path) else ""
        r["metrics"] = parse_metrics(r["agent"], text)
        rows.append(r)
    return rows


def mean(xs):
    xs = [x for x in xs if x is not None]
    return statistics.mean(xs) if xs else None


def main():
    rows = load()
    agents = sorted({r["agent"] for r in rows})
    tasks  = sorted({r["task"]  for r in rows})

    # cell[(agent,task,cond)] = list of total tokens
    cell = defaultdict(list)
    cost = defaultdict(list)
    models = {}
    skipped = 0
    for r in rows:
        models[r["agent"]] = r.get("model", "?")
        if r.get("exit_code", 1) != 0 or not r["metrics"]["total"]:
            skipped += 1            # failed/zero-token run — don't pollute means
            continue
        m = r["metrics"]
        cell[(r["agent"], r["task"], r["condition"])].append(m["total"])
        cost[(r["agent"], r["task"], r["condition"])].append(m["cost"])

    def pct(without, with_):
        if not without or not with_:
            return None
        return round((without - with_) / without * 100, 1)

    lines = ["# Benchmark Results", "",
             "_Generated by `analyze.py` from committed `runs/`. "
             "Read `benchmarks/README.md` for methodology and limitations._", ""]
    lines.append(f"- Agents / models: " +
                 ", ".join(f"**{a}** (`{models.get(a,'?')}`)" for a in agents))
    reps = max((r["rep"] for r in rows), default=0)
    lines += [f"- Reps per cell: up to {reps}", f"- Tasks: {', '.join(tasks)}"]
    if skipped:
        lines.append(f"- Excluded {skipped} failed/zero-token run(s) from aggregation.")
    lines.append("")

    # Per-agent total-token table
    lines += ["## Total tokens — WITH vs WITHOUT policy", "",
              "| Agent | Task | Without | With | Reduction |",
              "|-------|------|--------:|-----:|----------:|"]
    agent_overall = defaultdict(lambda: [0.0, 0.0])  # [without_sum, with_sum]
    for a in agents:
        for t in tasks:
            wo = mean(cell.get((a, t, "without"), []))
            wi = mean(cell.get((a, t, "with"), []))
            red = pct(wo, wi)
            if wo is not None and wi is not None:
                agent_overall[a][0] += wo
                agent_overall[a][1] += wi
            def fmt(x): return "—" if x is None else f"{x:,.0f}"
            lines.append(f"| {a} | {t} | {fmt(wo)} | {fmt(wi)} | "
                         f"{'—' if red is None else str(red)+'%'} |")

    # Per-agent rollup
    lines += ["", "## Per-agent overall reduction", "",
              "| Agent | Without (sum of task means) | With | Reduction |",
              "|-------|---------------------------:|-----:|----------:|"]
    chart_data = []
    for a in agents:
        wo, wi = agent_overall[a]
        red = pct(wo, wi) if wo else None
        chart_data.append((a, wo, wi))
        lines.append(f"| {a} | {wo:,.0f} | {wi:,.0f} | "
                     f"{'—' if red is None else str(red)+'%'} |")

    lines += ["", "![token reduction](chart.svg)", ""]
    open(os.path.join(HERE, "RESULTS.md"), "w").write("\n".join(lines) + "\n")
    write_chart(chart_data)
    print("wrote RESULTS.md and chart.svg")


def write_chart(data):
    """Grouped bar chart: per agent, without vs with total tokens."""
    W, H, pad = 640, 360, 50
    maxv = max([max(wo, wi) for _, wo, wi in data] + [1])
    bw = 26
    gap = 90
    svg = [f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 {W} {H}" font-family="sans-serif">']
    svg.append(f'<text x="{W/2}" y="24" text-anchor="middle" font-size="16" font-weight="700">Total tokens: without vs with policy</text>')
    base = H - pad
    svg.append(f'<line x1="{pad}" y1="{base}" x2="{W-pad}" y2="{base}" stroke="#999"/>')
    for i, (a, wo, wi) in enumerate(data):
        x = pad + 30 + i * gap
        for j, (label, v, color) in enumerate([("without", wo, "#bbb"), ("with", wi, "#3b82f6")]):
            h = (v / maxv) * (H - 2 * pad) if maxv else 0
            bx = x + j * (bw + 4)
            svg.append(f'<rect x="{bx}" y="{base - h}" width="{bw}" height="{h}" fill="{color}"/>')
            svg.append(f'<text x="{bx + bw/2}" y="{base - h - 4}" text-anchor="middle" font-size="9">{v:,.0f}</text>')
        red = (wo - wi) / wo * 100 if wo else 0
        svg.append(f'<text x="{x + bw}" y="{base + 16}" text-anchor="middle" font-size="11" font-weight="600">{a}</text>')
        svg.append(f'<text x="{x + bw}" y="{base + 30}" text-anchor="middle" font-size="9" fill="#16a34a">-{red:.0f}%</text>')
    open(os.path.join(HERE, "chart.svg"), "w").write("\n".join(svg) + "\n</svg>\n")


if __name__ == "__main__":
    main()
